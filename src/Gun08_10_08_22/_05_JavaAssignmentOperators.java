package Gun08_10_08_22;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {
        int a=10;
        a=a+5;
        a+=5;
        System.out.println("a = " + a);

        a=a-5;
        a-=5;
        System.out.println("a = " + a);

        a=a*5;
        a*=5;
        System.out.println("a = " + a);

        a=a/5;
        a/=5;
        System.out.println("a = " + a);

    }
}

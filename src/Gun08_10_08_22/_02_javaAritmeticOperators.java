package Gun08_10_08_22;

public class _02_javaAritmeticOperators {
    public static void main(String[] args) {
        int sayac=0;

        sayac=sayac+1;// sayacın değerini 1 artırır
        sayac++ ; // sayacın değerini 1 artırır
        ++sayac ; // sayacın değerini 1 artırır

        sayac=sayac-1 ; // sayacın değerini 1 azaltır
        sayac-- ; // sayacın değerini 1 azaltır
        --sayac ; // sayacın değerini 1 azaltır

        /***********************************/
        int sonuc=0;
        sayac=0;
        sonuc=sayac++ ;// ++ veya -- ler sağda ise yokmuş gibi işlemi yapacağız sonra ++ veya -- leri yapacağız.






    }
}

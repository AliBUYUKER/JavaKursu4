package Gun06_08_08_22;

public class _12_StringLastIndexOf {
    public static void main(String[] args) {
        // indexOf gibi çalışır bu aramaya baştan başlar
        // lastIndexof ise : aramaya sonradan başlar.
        // LastIndexof 2. boşluk için kullanılabilir.

        String s1="Merhaba";
        System.out.println("Baştan sona doğru a yı arar = "+s1.indexOf("a"));
        System.out.println("Sondan Başa doğru a yı arar = " + s1.lastIndexOf("a"));
        // index her zaman tek arama yönü değişir.







    }
}

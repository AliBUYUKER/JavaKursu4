package Gun06_08_08_22;

public class _11_StringlsEmpty {
    public static void main(String[] args) {
        // isEmpty: bir stringin boş olup olmadığını boolean olrak verir.

        String s1="Merhaba";
        System.out.println("s1.isEmpty() = "+ s1.isEmpty()); // false

        String s2=" "; // boşluk karakteri A gibi bir karakterdir
        System.out.println("s2.isEmpty() = "+s2.isEmpty());

        String s3="";
        System.out.println("s3 = " + s3.isEmpty());// true





    }
}

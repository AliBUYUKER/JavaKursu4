package Gun14_18_08_22;

public class _01_forLoop {
    public static void main(String[] args) {
        // 1den 5 e kadar olan sayıları ekrana yazdırınız.
        int i=1;
        while (i<=5){
            System.out.println("w_i = " + i);
            i++;
        }
        // 1 2 3 4 5
        //for döngüsü
        for (int j=1;j<=5;j++)
            System.out.println("f_j = " + j);



    }
}

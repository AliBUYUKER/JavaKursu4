package Gun14_18_08_22;

public class _02_forLoop {
    public static void main(String[] args) {
        // 0 den 10(dahil) kadar sayıları ekrana yazdırınız.
        // 10 dan 0(dahil) kadar sayıları ekrana yazdırınız.(ayri bir diğer for)
        for (int i=0;i<=10;i++)
            System.out.println("i = " + i);// i 10'dan küçük olduğu sürece devam edecek

        for (int j=10;j>=0;j--)
            System.out.println("j = " + j);// j 0'dan büyük olduğu sürece devam edecek







    }
}

package Gun15_19_08_22;

public class _10_GununSorusu {
    public static void main(String[] args) {
        // Aşağıdaki görüntüyü veren programı yazınız.
        //    *
        //   **
        //  ***
        // ****
        //*****
        for (int i=1;i<=5;i++){
            for (int j=5;j>=0;j--){
                if (j>=i){
                    System.out.print(" ");
            }else System.out.print("*");
        }
            System.out.println();}






    }
}
